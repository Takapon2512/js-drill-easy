//複数のオブジェクト要素を持つ配列の定義
const object = [
    {id: 1, name: 'ジョン', age: 15},
    {id: 2, name: 'ボブ', age: 20},
    {id: 3, name: 'マイケル', age: 15}
]
console.log(object)

//配列オブジェクト複数のオブジェクト要素を持つ配列からオブジェクト取得
console.log(object[1]);

//複数のオブジェクト要素を持つ配列からプロパティの値取得
console.log(object[2].age)

